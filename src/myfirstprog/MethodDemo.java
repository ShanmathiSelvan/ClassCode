package myfirstprog;

public class MethodDemo {
	
	public int add(int a,int b) {
		int sum = a+b;
		return sum;
	}

	public int subtract(int x,int y) {
		int minus=x-y;
		return minus;

	}
	public int multiply(int a,int b) {
		int c =a*b;
		return c;
	}
	
	public int divide(int a,int b) {
		int div = a/b;
		return div;
	}
	
}
